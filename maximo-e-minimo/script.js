function maximo(num1, num2) {
  if (num1 > num2) {
    console.log(`O maior número é ${num1}`)
  } else {
    console.log(`O maior número é ${num2}`)
  }
}

function minimo(num1, num2) {
  if (num1 < num2) {
    console.log(`O menor número é ${num1}`)
  } else {
    console.log(`O menor número é ${num2}`)
  }
}

